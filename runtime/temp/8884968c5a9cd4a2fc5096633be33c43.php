                    if (curpage < pagenum) {
                        _commentBtnMore.html('<a href="javascript:showComList(' + comment_type + ',' + relate_id + ',' + (page + 1) + ')">查看更多</a>');
                    } else if (curpage == pagenum) {
                        _commentBtnMore.find('a').remove();
                    }
                    fixCommentAjax(comment_type, relate_id, page);
                }
            }
        }
    });
}

