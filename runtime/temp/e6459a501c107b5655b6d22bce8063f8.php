function addComment(obj, fid) {
    var login_uid = "193654";
    if (login_uid == '' || login_uid == 0) {
        loginPop("http://www.daizhe.cn/share/detail/id/19438#start_reply");
        return false;
    }
    var content = $.trim($("#comment").val());
    var content2 = $.trim($("#comment2").val());
    if (content == '' || content == content2) {
        //showMsg(obj, '评论内容不能为空！');
        alert('评论内容不能为空！');
        return false;
    }
    if (content.length > 500) {
        //showMsg(obj, '字符过长！');
        alert('字符过长！');
        return false;
    }
    //之后也可以改成：文本输入500字之后就不能输入--sina
    $.ajax({
        type: 'post',
        url: '/comment/do/',
        data: {
            "ajax": "add",
            "comment_type": "4",
            "relate_id": 19438,
            "comment_fid": fid,
            "content": content.replace(content2, '')
        },
        dataType: "json",
        success: function(result) {
            if (result.code == "1") {
                $('.com_liuyanList').html('');
                showComList(1);
            } else if (result.code == "-1") {
                loginPop("http://www.daizhe.cn/share/detail/id/19438#start_reply");
                return false;
            } else {
                alert(result.msg);
                return false;
            }
        }
    });
}

