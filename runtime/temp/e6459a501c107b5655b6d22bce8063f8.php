        var st = $(document).scrollTop(),
            winh = $(window).height();
        (st > 1000) ? $backToTopEle.show(): $backToTopEle.hide();
        //IE6下的定位
        if (!window.XMLHttpRequest) {
            $backToTopEle.css("top", st + winh - 166);
        }
    };
    $(window).bind("scroll", $backToTopFun);
    $backToTopFun();
    $(".backToTop").hover(function() {
        $(this).addClass("on2")
    }, function() {
        $(this).removeClass("on2")
    })
}
